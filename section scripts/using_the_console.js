// Logging Text onto the console
console.log('Hello World');

// Logging Numbers onto the console
console.log(123);

// Logging an Object onto the console
console.log({ Name: 'Abbaas', Age: 12 })

// Logging an Array onto the console
console.log(['Sarah', 'John', 'Carter'])

// Logging Booleans onto to the console
console.log(true, false)

// Logging type of undefined onto the console
console.log(undefined)

// Logging type of Null onto the console
console.log(null)

// Logging type of NaN
console.log(NaN)

// Logging an object in the form of a table by using console.table()
console.table({ a: 1, b: 2, c: 3 })

